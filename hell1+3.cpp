#include <chrono>
#include <iostream>
#include <unordered_map>

std::unordered_map<int, long long int> cache;

void sum_up_to_n(int n) {
  if (cache.find(n) != cache.end()) {
    std::cout << "Sum of integers up to " << n << ": " << cache[n] << '\n';
    return;
  }

  // Initialize sum variable
  long long int sum = 0;

  // Loop through integers from 1 to n
  for (int i = 1; i <= n; ++i) {
    // Add current integer to sum
    sum += i;
  }

  cache[n] = sum;

  std::cout << "Sum of integers up to " << n << ": " << sum << '\n';
}

